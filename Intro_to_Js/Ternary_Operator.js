// Unary :- one
// binary :- two
// ternary :- three :- 3 expressions/situation involved with it.

let y = ((10>5) ? (10): (7));
console.log(y);

let x = ((10<5) ? (10): (7));
console.log(x);

let a = ((true) ? (2+3): (2-3));
console.log(a);

let b = ((false) ? (2+3): (2-3));
console.log(b);

let u = 10;
let h = ((3<1)?(2-3):(++u));
console.log(h);